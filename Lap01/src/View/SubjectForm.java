    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        SubjectEnum subjectEnum = SubjectEnum.getCode(cboSubject.getSelectedItem().toString());
        if (subjectEnum != null) {            
            // TAO DOI TUONG SUBJECT-MON HOC
            Subject subject = new Subject(subjectEnum.toString(), subjectEnum.getSubjectName(), txtSemester.getText(), chkStatus.isSelected());
            listSubject.add(subject);
            // Goi Service them doi tuong vao bang
            fillDataToTable();
            // Goi Service thay doi du lieu trong file
            service.changeToFile(listSubject);
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int index = tblListSubject.getSelectedRow();
        if (index >= 0 && !listSubject.isEmpty()) {
            // xoa du lieu khoi danh sach 
            listSubject.remove(index);
            // Goi service xoa object khoi bang
            service.changeToFile(listSubject);
            // Do du lieu vao bang
            fillDataToTable();
        } else {
            JOptionPane.showConfirmDialog(this, "Khong the thuc hien xoa");
        }
        
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnOpenFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOpenFileActionPerformed
        // TODO add your handling code here:
        // Goi service mo file
        listSubject = service.getDataFromFile();
        if (listSubject == null) {
            JOptionPane.showMessageDialog(this, "Không tim thay file");
        } else if (listSubject.isEmpty()) {
            JOptionPane.showMessageDialog(this, "File khong co du lieu");
        } else {
            // Do du lieu vao bang
            fillDataToTable();
        }
        
    }//GEN-LAST:event_btnOpenFileActionPerformed

