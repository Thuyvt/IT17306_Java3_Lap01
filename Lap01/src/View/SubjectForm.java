    public void fillDataToTable() {
        defaultTable.setRowCount(0);
        for (Subject sj : listSubject) {
            defaultTable.addRow(new Object[] {
                sj.getCode(), sj.getName(), sj.getSemester(),
                sj.isStatus()
            });
        }
    }
    

